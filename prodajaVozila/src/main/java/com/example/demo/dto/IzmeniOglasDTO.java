package com.example.demo.dto;

public class IzmeniOglasDTO {

    private String idoglas;
    private String cena;
    private String marka;
    private String model;
    private String tekst;
    private String vrstaVozila;

    public String getIdoglas() {
        return idoglas;
    }

    public void setIdoglas(String idoglas) {
        this.idoglas = idoglas;
    }

    public String getCena() {
        return cena;
    }

    public void setCena(String cena) {
        this.cena = cena;
    }

    public String getMarka() {
        return marka;
    }

    public void setMarka(String marka) {
        this.marka = marka;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getTekst() {
        return tekst;
    }

    public void setTekst(String tekst) {
        this.tekst = tekst;
    }

    public String getVrstaVozila() {
        return vrstaVozila;
    }

    public void setVrstaVozila(String vrstaVozila) {
        this.vrstaVozila = vrstaVozila;
    }
}
